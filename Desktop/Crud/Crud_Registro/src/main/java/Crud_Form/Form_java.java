     public void Eliminar(){
         int fila = TablaDatos.getSelectedRow();
         
         if(fila == -1){
             JOptionPane.showMessageDialog(null, "fila no selecionada");
             Limpiar();
         }else{
             String sql = "delete from personacrud where id =" + id;
             
             try {
                 cn = con.getconexion();
                 st = cn.createStatement();
                 st.executeUpdate(sql);
                 JOptionPane.showMessageDialog(null, "usuario correctamente elimiado");
                 Limpiar();
             } catch (Exception e) {
                 System.out.println("Error en el metodo eliminar" + e);
             }
         }
     }
     
